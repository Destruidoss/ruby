=begin
Em Ruby, cada item dentro de um array está em uma posição numerada chamada índice . O primeiro item está no índice 0, o segundo item está no índice 1 e assim por diante. Podemos acessar o iésimo elemento de um array colocando o índice entre colchetes após invocar o nome do array; isso é conhecido como acesso por índice.

=end

example = ["Car", "Boar", 45, 9.9, true]

#Para uma matriz chamada `example`, você pode recuperar um item de um índice específico referenciando seu índice.

puts example[2] # => 45
puts example[0] # => Car